/**
 * Путь к файлу из `public/` с учётом базового пути сборки.
 *
 * Абсолютные ссылки вида `/assets/logo.svg` работают, только пока сайт живёт в
 * корне домена. На GitHub Pages проектный сайт отдаётся из подпапки
 * (`/имя-репозитория/`), и такие ссылки уходят в корень, где ничего нет.
 * Vite подставляет базовый путь только в то, что проходит через сборщик;
 * строковые литералы в JSX и в загрузчиках Three.js он не трогает — отсюда
 * этот хелпер.
 *
 * `BASE_URL` всегда заканчивается слэшем, поэтому у пути его быть не должно.
 */
export const asset = (path: string) => `${import.meta.env.BASE_URL}${path.replace(/^\//, '')}`;
